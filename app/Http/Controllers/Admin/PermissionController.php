    {
        $permissions = Permission::all();
        if (request()->ajax()) {
            return Datatables::of($permissions)
                ->addIndexColumn()
                ->addColumn('action', function ($row) {
                    $btn = '<a href="" class="edit btn btn-warning btn-sm mb-1 mr-1">Edit</a>';
                    $btn .= '<a href="" class="remove btn btn-danger btn-sm mb-1">Delete</a>';
                    return $btn;
                })
                ->make(true);
        }
        return view('admin.permissions.index');
    }

